class Pokemon{
    set nome(valor){ this._nome = valor; }
    get nome(){ return this._nome; }

    set tipo(valor){ this._tipo = valor; }
    get tipo(){ return this._tipo; }

    set hp(valor){ this._hp = valor; }
    get hp(){ return this._hp; }

    set ataque(valor){ this._ataque = valor; }
    get ataque(){ return this._ataque; }

    set defesa(valor){ this._defesa = valor; }
    get defesa(){ return this._defesa; }
}